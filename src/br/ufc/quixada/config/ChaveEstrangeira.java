package br.ufc.quixada.config;

public class ChaveEstrangeira {

	private String nomeChave;
	private String nomeAtributo;
	private String nomeClasseAtributo;

	public String getNomeChave() {
		return nomeChave;
	}

	public void setNomeChave(String nomeChave) {
		this.nomeChave = nomeChave;
	}

	public String getNomeAtributo() {
		return nomeAtributo;
	}

	public void setNomeAtributo(String nomeAtributo) {
		this.nomeAtributo = nomeAtributo;
	}

	public String getNomeClasseAtributo() {
		return nomeClasseAtributo;
	}

	public void setNomeClasseAtributo(String nomeClasseAtributo) {
		this.nomeClasseAtributo = nomeClasseAtributo;
	}
	
}
